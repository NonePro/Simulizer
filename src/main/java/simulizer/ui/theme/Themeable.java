package simulizer.ui.theme;

/**
 * All themeable component should implement this interface
 * 
 * @author Michael
 *
 */
public interface Themeable {

	/**
	 * Sets the component to this theme
	 * 
	 * @param theme
	 *            the theme to set the component as
	 */
	public void setTheme(Theme theme);
}
